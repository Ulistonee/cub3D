#include "cub3d.h"

int			is_str(char *str)
{
	if (ft_strlen(str) != 6)
		return (0);
	else
	{
		if (str[0] != '-')
			return (0);
		if (str[1] != '-')
			return (0);
		if (str[2] != 's')
			return (0);
		if (str[3] != 'a')
			return (0);
		if (str[4] != 'v')
			return (0);
		if (str[5] != 'e')
			return (0);
	}
	return (1);
}
